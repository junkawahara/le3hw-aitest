// seg7dec.v - 7セグメントLEDデコーダ (課題1)
// 4ビット16進数入力を8ビットセグメント出力に変換する組み合わせ回路
//
// セグメント配置 (MU500-RK):
//       out[6](a)
//      ---------
//     |         |
// out[1](f)  out[5](b)
//     |         |
//      ---------
//       out[0](g)
//     |         |
// out[2](e)  out[4](c)
//     |         |
//      ---------
//       out[3](d)    out[7](dp)
//
// Active LOW: 0 = 点灯, 1 = 消灯

module seg7dec(
    input  [3:0] din,
    output [7:0] seg
);

assign seg = (din == 4'h0) ? 8'b10000001 :
             (din == 4'h1) ? 8'b11001111 :
             (din == 4'h2) ? 8'b10010010 :
             (din == 4'h3) ? 8'b10000110 :
             (din == 4'h4) ? 8'b11001100 :
             (din == 4'h5) ? 8'b10100100 :
             (din == 4'h6) ? 8'b10100000 :
             (din == 4'h7) ? 8'b10001111 :
             (din == 4'h8) ? 8'b10000000 :
             (din == 4'h9) ? 8'b10000100 :
             (din == 4'hA) ? 8'b10001000 :
             (din == 4'hB) ? 8'b11100000 :
             (din == 4'hC) ? 8'b10110001 :
             (din == 4'hD) ? 8'b11000010 :
             (din == 4'hE) ? 8'b10110000 :
                             8'b10111000 ; // F

endmodule
